#ifndef _KB_H         //防止重复定义（_KB_H  开头)
#define _KB_H

#include "common.h"    //包含公共要素头文件
#include "gpio.h"      //包含gpio头文件

//键盘（KB）硬件接线
#define m1  (PTA_NUM | 2)   //4根行线硬件连接
#define m2  (PTA_NUM | 5)
#define m3  (PTA_NUM | 1)
#define m4  (PTA_NUM | 4)
#define n1  (PTA_NUM | 12)  //4根列线硬件连接
#define n2  (PTA_NUM | 13)
#define n3  (PTA_NUM | 16)
#define n4  (PTA_NUM | 17)


//=================接口函数声明=========================================
//=====================================================================
//函数名称：KBInit
//函数返回：无
//参数说明：无
//功能概要：初始化键盘模块。
//=====================================================================
void KBInit(void);

//=====================================================================
//函数名称：KBScanN
//函数返回：键值,无键按下返回0xFF
//参数说明：重复扫描键盘的的次数（scan_cnt），建议1~20之间
//功能概要：多次扫描键盘,返回键值，scan_cnt小于等于1，直接返回扫描1次键值，否则再
//        继续扫描到（scan_cnt*0.5）次相同的键值,且键值不为0xFF时返回该键值，
//        否则返回最后一次扫描值
//======================================================================
uint_8 KBScanN(uint_8 KB_count);

//=====================================================================
//函数名称：KBDef
//函数返回：无
//参数说明：键值valve 
//功能概要：键值转为定义值函数。
//=====================================================================
uint_8 KBDef(uint_8 valve);

#endif     //防止重复定义（结尾）

