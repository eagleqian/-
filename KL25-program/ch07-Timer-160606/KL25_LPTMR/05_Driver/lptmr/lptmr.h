#ifndef _LPTMR_H
#define _LPTMR_H
#include "common.h"

//LPTMR时钟源
#define LPOCLK      1
#define IRC32KCLK   2
#define IRC4MCLK    3
#define OSCERCLK    4

//获取中断标志
#define LPTMR_GET_FLAG    ((LPTMR0_CSR & LPTMR_CSR_TCF_MASK) == \
                            LPTMR_CSR_TCF_MASK)
//清中断标志
#define LPTMR_CLEAR_FLAG    (LPTMR0_CSR |= LPTMR_CSR_TCF_MASK)

//============================================================================
//函数名称：lptmr_init
//函数返回：无
//参数说明：clktype，指明时钟源类别，参照lptmr.h中LPTMR时钟源的宏定义。
//功能概要：LPTMR模块初始化，配置LPTMR工作的时钟源，中断时间为1秒。
//调用举例：lptmr_init(LPOCLK);配置LPTMR模块时钟源为LPOCLK时钟。
//============================================================================
void lptmr_init(uint_8 clktype);

//============================================================================
//函数名称：lptmr_enable_int
//函数返回：无
//参数说明：无
//功能概要：使能LPTMR模块中断。
//调用举例：lptmr_enable_int();使能LPTMR模块中断
//============================================================================
void lptmr_enable_int();

//============================================================================
//函数名称：lptmr_disable_int
//函数返回：无
//参数说明：无
//功能概要：禁止LPTMR模块中断
//调用举例：lptmr_disable_int();禁止LPTMR模块中断
//============================================================================
void lptmr_disable_int();


#endif 

