.include "include.S"

#start 函数定义区域
.global UART1_IRQHandler
.type  UART1_IRQHandler, function
#end  函数定义区域

.section .text

#===========================================================================
# 函数名称：UART1_IRQHandler
# 函数返回：无
# 参数说明：无
# 功能概要：UART1中断服务例程，将接收的单字节数据发送出去
#===========================================================================
UART1_IRQHandler:
    push {r4-r7,lr}
    cpsid i
    
    ldr r0,=UART1
    bl uart_re1
    
    ldr r0,=UART1
    bl uart_send1

    cpsie i
irq_end:    
    pop {r4-r7,pc}
