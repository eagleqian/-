//文件名称：cop.c

#include"cop.h"

//===========================================================================
//函数名称：WDOG_DISABLE
//函数返回：无
//参数说明：无
//功能概要：禁用看门狗功能
//===========================================================================
void WDOG_DISABLE()
{
	SIM_COPC = 0x00u;
}

//===========================================================================
//函数名称：WDOG_ENABLE
//函数返回：无
//参数说明：无
//功能概要：开启看门狗功能
//===========================================================================
void WDOG_ENABLE()
{
	SIM_COPC |= SIM_COPC_COPT_MASK;  //看门狗复溢出复位时间设为 2^10 个内部时钟
}

//===========================================================================
//函数名称：FEED_DOG
//函数返回：无
//参数说明：无
//功能概要：实现定时喂狗功能
//===========================================================================
void FEED_DOG()
{
	//定时喂狗
	SIM_SRVCOP = 0x55;
	SIM_SRVCOP = 0xAA;
}
